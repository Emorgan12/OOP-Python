x, y, z = "Orange", "Banana", "Cherry" # Many Values to Multiple Variables
print(x)
print(y)
print(z)

print("--------------------------------------------------------")

x = y = z = "Mango" # One Value to Multiple Variables
print(x)
print(y)
print(z)

print("--------------------------------------------------------")

fruits = ["apple", "banana", "cherry"] # Unpack a Collection. for more see 7oop-lists.py
x, y, z = fruits
print(x)
print(y)
print(z)